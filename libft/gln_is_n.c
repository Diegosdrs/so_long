#include "libft.h"

int	is_n(char *buffer)
{
	int	i;

	i = 0;
	while (buffer[i] != '\0')
	{
		if (buffer[i] == '\n')
			return (1);
		i++;
	}
	if (buffer[i] == '\n')
		return (1);
	return (0);
}
